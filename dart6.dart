// arithmetic operator
void main() {
  int a = 10;
  int b = 5;

  int sum = a + b;
  print('Sum: $sum');

  int difference = a - b;
  print('Difference: $difference');

  int product = a * b;
  print('prodict: $product');

  double quotient = a / b;
  print('Quotient: $quotient');

  int reminder = a % b;
  print('Reminder: $reminder');

  int intDivision = a ~/ b;
  print('Int DivisionL $intDivision');

  int c = 10;
  c++;
  print('Post-increment: $c');

  int d = 10;
  ++d;
  print('Pre-increment: $d');

  int e = 10;
  e--;
  print('Post-decrement: $e');

  int f = 10;
  print('Pre-decrement: $f');
}